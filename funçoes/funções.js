/* Funções e o que são? bloco de código que pode ser executado e reutilizado .
valores podem ser passados por uma função e a mesma retorna outro valor.*/

function areaQuadrado(lado) {
  return lado * lado;
}

areaQuadrado(4) // 16

areaQuadrado(5) // 25

areaQuadrado(2) // 4

