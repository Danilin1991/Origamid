// condicionais If e Else
//verificar se se uma expressão é verdadeira com if, caso contrario o else é ativado.

var possuiGraduacao = true;

if (possuiGraduacao) {
  console.log('Possui Graduacao');
} else {
  console.log('Nao possui Graduacao');
}

// retorna possui graduação, pois o valor da variável possuiGraduacao é true ,o valor dos parenteses será avaliado em false ou true